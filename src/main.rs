#![warn(clippy::all, rust_2018_idioms)]

use anyhow::Error;
use learn_wgpu::App;
use winit::event_loop::EventLoop;

fn main() -> anyhow::Result<(), Error> {
    {
        env_logger::init();
    }

    let event_loop = EventLoop::with_user_event().build()?;
    let mut app = App::new();
    event_loop.run_app(&mut app)?;
    Ok(())
}
